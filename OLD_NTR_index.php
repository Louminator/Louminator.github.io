          <h2>Activities</h2>
          <h4>Research</h4>
          <p>I am interested in a solving a variety of problems that arise in fluid dynamics, computational methods, swarming and wireless networks.  
          On the surface, it may look like these problems have nothing to do with one another, but mathematically, they are all connected.  
          In the broad sense, the mathematical problems that interest me involve the deep connections between discrete and continuous mathematical structures.  
          For instance, I like to solve partial differential equations that describe fluid motion using particle methods.  
          The fluid motion is described by continuous equations.  My numerical methods capture this behavior with discrete particles.</p>
