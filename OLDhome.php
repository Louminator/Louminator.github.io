<h3>Recent items</h3>
<p>
<a href="http://dx.plos.org/10.1371/journal.pone.0040084">
Quantifying and Tracing Information Cascades in Swarms</a>.<br>
Work with Rose Wang, Jen Miller, Joe Lizier and Mikhail Prokopenko in 
<em>PLoS ONE</em> measuring theoretical 
information transfer in swarms. We 
provide the first direct information-theoretic evidence for the long-held conjecture 
that the information cascades occur in waves rippling through the swarm. 
Our experiments also exemplify how features of swarm dynamics, such as cascades’ wavefronts,  
can be filtered and predicted. 
</p>
<p>
<a href="recent/SIAM-AN12-slides.pdf">Particle methods with deforming basis functions and field interpolation.</a><br>
Slides from a minisymposium <a href="http://meetings.siam.org/sess/dsp_programsess.cfm?SESSIONCODE=14809">
"Challenges and Synthesis in Meshfree Methods"</a> organized with Prof. Toby Driscoll 
for the 2012 SIAM Annual Meeting. Followup discussions are hosted at 
<a href="http://www.meshfreedynamics.org">The Center for Meshfree Dynamics</a>
site.
</p>
<p>
<a href="publications/BMB12.pdf">A Continuum Three-Zone Model for Swarms</a>.<br>
Work with Jennifer Miller, Allison Kolpas and Plinio Neto in <i>Bulletin on Mathematical Biology</i> where we
establish and analyze a self-consistent, continuum model for swarming.
</p>
<p><a href="http://ltcessays.wordpress.com/category/essays/">Using Wikis to Promote Active Inquiry in First 
Semester Calculus, in Teaching with Technology Volume 2: The Stories Continue.</a>
A fun project where I engaged students using images 
to create their own related rates and optimization problems that were
a big more inspiring than the problems in text.
</p>
<p><a href="publications/ECAL11.pdf">Measuring Information Storage and Transfer in Swarms</a>
 in Proc. Eleventh European Conference on the Synthesis and Simulation of Living Systems 
 (ECAL 2011), Paris, 2011. Published in Advances in Artificial Life, ECAL 2011. Work with Rosalind Wang, Jen Miller,
 Joseph Lizier and Mikhail Prokopenko on information transfer in swarms.
</p>
<p>
<a href="publications/SI11.pdf">Slime mold inspired routing protocols for wireless sensor networks</a>.<br>
Work with Ke Li, Claudio Torres, Kyle Thomas and Chien-Chung Shen in <i>Swarm Intelligence</i> where we
develop a routing protocol for sensor networks based on slime mold.
</p>
<p><a href="publications/JCP10.pdf">Global ﬁeld interpolation for particle methods</a>.<br>
Work with Lorena Barba in <i>Journal of Computational Physics</i> on field interpolation (replacing one set of particles with another
without inducing significant field errors).
</p>
<p>
<a href="http://siamdl.aip.org/vsearch/servlet/VerityServlet?KEY=SIAMDL&smode=results&sort=rel&maxdisp=25&threshold=0&pjournals=SIAMDL%2CMMSUBT%2CSJADAY%2CSMJMAP%2CSMJCAT%2CSJCODC%2CSJDMEC%2CSJISBI%2CSJMAAH%2CSJMAEL%2CSJNAAM%2CSJOPE8%2CSJOCE3%2CSIREAD%2CTPRBAU&possible1=rossi&possible1zone=article&OUTLOG=NO&viewabs=SJOCE3&key=DISPLAY&docID=1&page=0&chapter=0">
